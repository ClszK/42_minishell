#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*head;
	t_list	*node;
	void	*test;

	head = NULL;
	if (lst == NULL)
		return (NULL);
	while (lst)
	{
		test = f(lst->content);
		node = ft_lstnew(test);
		if (node == NULL)
		{
			del(test);
			ft_lstclear(&head, del);
			return (NULL);
		}
		ft_lstadd_back(&head, node);
		lst = lst->next;
	}
	return (head);
}
